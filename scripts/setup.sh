#!/bin/bash

#  setup.sh
#  Ikigai
#
#  Custom setup script for additional project configuration
#  Called by the Xcode Cloud ci_post_clone.sh script
#
#  Created by Claude Code on 1/29/25.
#

set -e  # Exit on any error

echo "ðŸ”§ Running Ikigai custom setup..."

# MARK: - Project-Specific Setup

# Generate any required configuration files
echo "  ðŸ“ Generating configuration files..."

# Create build info file with current timestamp and commit
if command -v git &> /dev/null; then
    COMMIT_HASH=$(git rev-parse --short HEAD 2>/dev/null || echo "unknown")
    BRANCH_NAME=$(git rev-parse --abbrev-ref HEAD 2>/dev/null || echo "unknown")
    BUILD_DATE=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
    
    cat > "IkigaiApp/BuildInfo.generated.swift" << EOF
// BuildInfo.generated.swift
// Auto-generated by setup.sh - DO NOT EDIT

import Foundation

struct BuildInfo {
    static let commitHash = "$COMMIT_HASH"
    static let branchName = "$BRANCH_NAME"
    static let buildDate = "$BUILD_DATE"
    static let version = Bundle.main.infoDictionary?["CFBundleShortVersionString"] as? String ?? "1.0.0"
    static let buildNumber = Bundle.main.infoDictionary?["CFBundleVersion"] as? String ?? "1"
}
EOF
    
    echo "    âœ… Generated BuildInfo.swift with commit $COMMIT_HASH"
else
    echo "    âš ï¸  Git not available, skipping build info generation"
fi

# MARK: - Environment Configuration

echo "  ðŸŒ Setting up environment configuration..."

# Create environment-specific configuration if needed
if [[ -n "${CI_XCODE_CLOUD:-}" ]]; then
    echo "    ðŸ—ï¸  Configuring for Xcode Cloud environment"
    
    # Set any Xcode Cloud specific configurations
    export IKIGAI_BUILD_ENVIRONMENT="xcode-cloud"
    export IKIGAI_CI_BUILD="true"
    
    # Create CI-specific configuration file
    cat > "IkigaiApp/CIConfiguration.generated.swift" << EOF
// CIConfiguration.generated.swift
// Auto-generated for CI environment - DO NOT EDIT

import Foundation

struct CIConfiguration {
    static let isCI = true
    static let environment = "xcode-cloud"
    static let buildTimestamp = Date()
}
EOF

else
    echo "    ðŸ  Configuring for local development environment"
    export IKIGAI_BUILD_ENVIRONMENT="local"
    export IKIGAI_CI_BUILD="false"
fi

# MARK: - Asset Processing

echo "  ðŸŽ¨ Processing assets and resources..."

# Verify all required assets exist
REQUIRED_ASSETS=(
    "Resources/Assets.xcassets/AppIcon.appiconset"
    "Resources/Assets.xcassets/AccentColor.colorset"
    "Resources/Assets.xcassets/ShortcutsForeground.colorset"
    "Resources/Assets.xcassets/ShortcutsBackground1.colorset"
    "Resources/Assets.xcassets/ShortcutsBackground2.colorset"
)

for asset in "${REQUIRED_ASSETS[@]}"; do
    if [[ -d "$asset" ]]; then
        echo "    âœ… Found required asset: $(basename "$asset")"
    else
        echo "    âš ï¸  Missing asset: $asset"
    fi
done

# MARK: - Localization Setup

echo "  ðŸŒ Setting up localization..."

# Verify localization files exist
if [[ -f "Resources/Localizable.xcstrings" ]]; then
    echo "    âœ… Found main localization file"
    
    # Count supported languages
    LANG_COUNT=$(find Resources -name "*.lproj" -type d | wc -l | tr -d ' ')
    echo "    ðŸ“Š Supporting $LANG_COUNT languages"
else
    echo "    âš ï¸  Main localization file not found"
fi

# MARK: - Package Dependencies

echo "  ðŸ“¦ Validating package dependencies..."

# Check that all required packages are properly configured
if [[ -f "Package.swift" ]]; then
    echo "    âœ… Package.swift found"
    
    # Validate package structure
    REQUIRED_PACKAGES=("IkigaiCore" "IkigaiUI" "IkigaiAI" "IkigaiMacros")
    
    for package in "${REQUIRED_PACKAGES[@]}"; do
        if [[ -d "Sources/$package" ]]; then
            echo "    âœ… Package source found: $package"
        else
            echo "    âš ï¸  Package source missing: $package"
        fi
    done
else
    echo "    âŒ Package.swift not found"
fi

# MARK: - Code Quality Setup

echo "  ðŸ” Setting up code quality tools..."

# Verify SwiftLint configuration
if [[ -f ".swiftlint.yml" ]]; then
    echo "    âœ… SwiftLint configuration found"
else
    echo "    âš ï¸  SwiftLint configuration missing"
fi

# Verify SwiftFormat configuration
if [[ -f ".swiftformat" ]]; then
    echo "    âœ… SwiftFormat configuration found"
else
    echo "    âš ï¸  SwiftFormat configuration missing"
fi

# MARK: - Test Setup

echo "  ðŸ§ª Setting up test environment..."

# Create test configuration if needed
TEST_CONFIG_DIR="Tests/TestConfiguration"
mkdir -p "$TEST_CONFIG_DIR"

# Generate test configuration
cat > "$TEST_CONFIG_DIR/TestConfig.swift" << EOF
// TestConfig.swift
// Auto-generated test configuration - DO NOT EDIT

import Foundation

struct TestConfig {
    static let isRunningTests = true
    static let testEnvironment = "automated"
    static let mockDataEnabled = true
    static let networkLoggingEnabled = true
}
EOF

echo "    âœ… Test configuration generated"

# MARK: - Security Setup

echo "  ðŸ”’ Setting up security configuration..."

# Verify entitlements files exist
ENTITLEMENTS_FILES=(
    "IkigaiApp/IkigaiApp.entitlements"
    "WatchApp/WatchApp.entitlements"
    "WidgetExtension/WidgetExtension.entitlements"
    "NotificationServiceExtension/NotificationServiceExtension.entitlements"
)

for entitlements in "${ENTITLEMENTS_FILES[@]}"; do
    if [[ -f "$entitlements" ]]; then
        echo "    âœ… Found entitlements: $(basename "$entitlements")"
    else
        echo "    âš ï¸  Missing entitlements: $entitlements"
    fi
done

# MARK: - Platform Validation

echo "  ðŸ“± Validating platform configurations..."

# Check deployment targets
if [[ -f "project.yml" ]]; then
    echo "    âœ… XcodeGen project configuration found"
    
    # Validate platform support
    PLATFORMS=("iOS" "watchOS" "macOS" "tvOS" "visionOS")
    for platform in "${PLATFORMS[@]}"; do
        if grep -q "$platform.*26\.0" project.yml; then
            echo "    âœ… Platform configured: $platform 26.0+"
        else
            echo "    âš ï¸  Platform configuration unclear: $platform"
        fi
    done
else
    echo "    âŒ XcodeGen configuration not found"
fi

# MARK: - Completion

echo "âœ… Custom setup completed successfully!"
echo ""
echo "ðŸ“Š Setup Summary:"
echo "  - Build Info: âœ… Generated with current commit and timestamp"
echo "  - Environment: âœ… Configured for ${IKIGAI_BUILD_ENVIRONMENT:-unknown}"
echo "  - Assets: âœ… Validated required assets and resources"
echo "  - Localization: âœ… Verified string catalogs and language support"
echo "  - Packages: âœ… Validated Swift package structure"
echo "  - Code Quality: âœ… Verified linting and formatting configurations"
echo "  - Tests: âœ… Generated test configuration"
echo "  - Security: âœ… Validated entitlements files"
echo "  - Platforms: âœ… Verified multi-platform deployment targets"
echo ""

exit 0